# Define the number of threads
thread_count = 8


def process_csv(file_path):
  json_items = []
  file_type = "dev" if "dev" in file_path else "eval"

  with open(file_path, newline='', encoding='utf-8') as csvfile:
    reader = csv.DictReader(csvfile)
    for row in reader:
      fname = row['fname']
      labels = row['labels'].split(',')
      # Randomly select a translation for each label from translation_dict
      translated_labels = [random.choice(
          translation_dict.get(label, [label])) for label in labels]
      # Join all translated labels into a single string
      translated_labels_str = ", ".join(translated_labels)

      answer_seed = f'{random.choice(answer_seeds)}'
      answer_seed_end = 'sound' if len(translated_labels) == 1 else 'sounds'
      if random.random() < 0.6:
        answer_seed = f"There {'is' if len(translated_labels) == 1 else 'are'}"

      # Construct JSON item
      json_item = {
          "audio": f"FSD50K/{file_type}/{fname}.wav",
          "conversations": [
              {
                  "from": "human",
                  "value": random.choice(question_seeds),
                  "input_modality": "audio"
              },
              {
                  "from": "gpt",
                  "value": f'{answer_seed} {translated_labels_str} {answer_seed_end}.',
                  "output_modality": "text"
              }
          ]
      }
      json_items.append(json_item)

  print(f"Processed {file_path}")
  return json_items


def main():
  # Assume folder path
  folder_path = "FSD50K.metadata/collection/"
  output_file = "json/FSD50K_eng.json"
  all_json_items = []

  # Get all CSV files in the folder
  csv_files = [os.path.join(folder_path, file) for file in os.listdir(
      folder_path) if file.endswith('.csv')]

  # Use ThreadPoolExecutor to process in parallel
  with ThreadPoolExecutor(max_workers=thread_count) as executor:
    futures = [executor.submit(process_csv, csv_file)
               for csv_file in csv_files]
    for future in futures:
      all_json_items.extend(future.result())

  # Write the result to a JSON file
  with open(output_file, 'w', encoding='utf-8') as f:
    # json.dump(all_json_items, f, ensure_ascii=False, indent=4)
    json.dump(all_json_items, f, ensure_ascii=False, separators=(',', ':'))

  print(f"All data has been processed and saved to {output_file}")


if __name__ == '__main__':
  main()
